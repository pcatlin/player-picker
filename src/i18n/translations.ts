export const getSupportedLanguage = (locale: string | undefined): SupportedLanguage => {
  const normalized = (locale ?? "en").toLowerCase();
  if (TRANSLATIONS[normalized as SupportedLanguage]) {
    return normalized as SupportedLanguage;
  }
  const prefix = normalized.split("-")[0] as SupportedLanguage;
  return TRANSLATIONS[prefix] ? prefix : "en";
};

export const translate = (
  language: SupportedLanguage,
  key: TranslationKey,
  params?: TranslationParams,
) => TRANSLATIONS[language][key](params);

